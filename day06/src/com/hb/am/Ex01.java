package com.hb.am;

public class Ex01 {

	public static void main(String[] args) {
		// 
		System.out.println("println"); // ()안의 내용을 출력하고 개행
		System.out.print("print"+'\n');	// ()안의 내용을 출력
		// \n 개행(엔터)
		// window - \r\n
		System.out.println("----------------------------");	// ()안의 내용을 출력
		// 기본 자료형(원시자료형)
		// 숫자(정수타입)
//			byte - -128~127
//			short
//			int	(4,기본자료타입)
//			long(8)
		// 숫자(실수타입) - 연산조심
//			float	(4)
//			double (8,기본자료타입)
		// 문자
		// 숫자로 저장-유니코드, 아스키코드(x)
//			char
//			''
//			(char)65  '\u0000'
//			문자열-""
		// 논리
//			boolean
//			true / false
		
		// boolean < byte < short < char < int < long < float < double
		
		// 연산
//		()
//		++,--						----> +1, -1
//		+,-,*,/,%					----> 오른쪽타입의 자료형
//		< , > , <= , >= , == , !=	----> true/false
//		&&, ||						----> true/false
//		= , += , -= , *= , /= , %=
		
		
	}

}





























